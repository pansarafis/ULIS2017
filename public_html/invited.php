        <h4 class="modal-title">About the intrinsic resistance variability in HfO2-based RRAM devices</h4>
      </div>
      <div class="modal-body">
        <h5>Abstract</h5>
        <p>
            Driven by the needs of traditional area scaling transistor architecture has evolved significantly in the last few CMOS technology nodes, with an evolution towards 3D transistor architectures such as finFET devices. Further scaling requires besides area reduction, power and performance improvements, which can be provided through a close interaction between design and technology. In this talk we will explore a few CMOS scaling pathways, with a focus on the interaction between design choices and transistor architecture. We will explore the transition from finFETs to gate-all-around (GAA) devices at advanced nodes, and the advantages of each choice. We will also have a look at the impact and challenges of introducing high mobility materials, such as Ge and III-V channels.
        </p>
