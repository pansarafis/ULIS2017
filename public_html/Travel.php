            <h3>Metro</h3>
            <p>Metro connections are available from the AIA Rail Station (adjacent to 
            the main terminal). The service is operating between 05:30 - 24:00, and the trains are departing 
            every half hour, at xx:00' and at xx:30'. The detailed schedule can be found <a href="http://www.stasy.gr/index.php?id=70&L=1">here</a>.
            Metro tickets (~ 8€) must be purchased from the station and validated before boarding the train. 
            The trip to Syntagma Sq. lasts 38'. The metro station that is closest to the conference venue is "EVANGELISMOS".
            </p>

            <h3>Suburban Railway</h3>
            <p>Metro connections are available from the AIA Rail Station (adjacent to 
                the main terminal). The service is operating between 05:30 - 23:11. 
                A complete schedule can be found <a href="http://www.athensairportbus.com/en/train/train-athens-airport.html">here</a>.
                In order to reach the Conference Venue or Syntagma Sq. you should take 
                the suburban railway up to Doukissis Plakentias, where you will change trains 
                and take Line 3 (blue line) towards Aghia Marina up to "EVANGELISMOS" 
                or "SYNTAGMA" stations, respectively.
            </p>

            <h3>Bus</h3>
            <p>Express bus routes operated by OASA (Public Bus Company) connect directly 
            the Athens (city center &amp; greater area) and Piraeus (port) with the Athens 
            International Airport (AIA) “Eleftherios Venizelos”. Service is provided on a 
            non-stop basis seven days a week including holidays (24/7 operation). All buses 
            disembark passengers at the Departures Level (inbound trips to airport) and 
            depart from the Arrivals Level (outbound trips from airport). Bus tickets (~5€) are 
            sold at the info/ticket-kiosk (located outside the Arrivals between Exits 4 and 
            5), or onboard (ask operator) at no extra cost. </p>

            <p>The most convenient bus route to reach Syntagma Sq. is <b>X95 SYNTAGMA  – ATHENS AIRPORT EXPRESS</b>.
               Direct Connection to Syntagma Square at Athens city center. 
               Link to metro line 2 station at: SYNTAGMA
               Link to metro line 3 stations at: NOMISMATOKOPEIO, ETHNIKI AMYNA, SYNTAGMA
               Link to tramway routes at: SYNTAGMA</p>


            <h3>Taxi</h3>
            <p>The color of taxis in Athens is yellow. When exiting Athens Airport you will find taxis waiting 
                between Exit 2 and Exit 3 at the arrival area. You should get in the queue and when your turn comes, 
                the policeman that controls the queue will signal you to get into the next available taxi.
            </p>
            <p>
                The fare from Athens airport to Athens city centre is fixed per taxi:
                <ul>
                    <li>Day time (05:00-24:00) Athens Airport to Athens city: 38 €</li>
                    <li>Night time (24:00-05:00) Athens Airport to Athens city: 50 €</li>
                </ul>
                The price includes totally everything. Luggage charge, tolls fee, VAT, meter start, airport charge etc. So, you don't have to pay anything more than the above.
                The same fixed fares apply when you pick a taxi from Athens centre to go to the airport.</p>
            </p>
